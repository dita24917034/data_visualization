# Commented out IPython magic to ensure Python compatibility.
# %%writefile app.py
import streamlit as st
import pandas as pd
import matplotlib.pyplot as plt
# 
# # Load data
df = pd.read_csv("data academic.csv")
# 
prog_code_mapping = {
    'BCH': 'Biochemistry',
    'BLD': 'Building technology',
    'CEN': 'Computer Engineering',
    'CHE': 'Chemical Engineering',
    'CHM': 'Industrial Chemistry',
    'CIS': 'Computer Science',
    'CVE': 'Civil Engineering',
    'EEE': 'Electrical and Electronics Engineering',
    'ICE': 'Information and Communication Engineering',
    'MAT': 'Mathematics',
    'MCB': 'Microbiology',
    'MCE': 'Mechanical Engineering',
    'MIS': 'Management and Information System',
    'PET': 'Petroleum Engineering',
    'PHYE': 'Industrial Physics-Electronics and IT Applications',
    'PHYG': 'Industrial Physics-Applied Geophysics',
    'PHYR' : 'Industrial Physics-Renewable Energy'
}

df['Prog Short'] = df['Prog Code'].map(prog_code_mapping)
# -------------------------------
# Sidebar Filters
# -------------------------------
st.sidebar.header("Filter Data")

# Filter Program Studi - dengan opsi "Semua"
prog_codes = sorted(df['Prog Code'].dropna().unique())
prog_options = ["All Program"] + prog_codes
selected_prog = st.sidebar.selectbox("Pilih Program Studi (Prog Code):", prog_options)

# Filter Tahun Masuk - dropdown tunggal dengan opsi "Semua Tahun"
available_yog = sorted(df['YoG'].dropna().unique())
yog_options = ["All Years"] + [str(y) for y in available_yog]
selected_yog = st.sidebar.selectbox("Pilih Tahun Masuk (YoG):", yog_options)

# -------------------------------
# Filter Data Berdasarkan Pilihan
# -------------------------------
filtered_df = df.copy()

# Terapkan filter Prog Code
if selected_prog != "All Program":
    filtered_df = filtered_df[filtered_df['Prog Code'] == selected_prog]

# Terapkan filter Tahun Masuk
if selected_yog != "All Years":
    filtered_df = filtered_df[filtered_df['YoG'] == int(selected_yog)]

# -------------------------------
# Main Dashboard
# -------------------------------
st.title("University ABC Student Dashboard")

# --- Kartu Statistik: Total Mahasiswa, Rata-rata CGPA, Tahun Masuk (YoG) ---
col1, col2, col3 = st.columns(3)

# Style umum untuk semua card
card_style = """
    <div style='padding: 16px; background-color: #f0f2f6; border-radius: 10px;
                text-align: center; box-shadow: 1px 1px 5px rgba(0,0,0,0.05);'>
        <h5 style='color: #333; font-size: 14px;'>{title}</h5>
        <h3 style='color: {color}; font-size: 20px; margin-top: 5px;'>{value}</h3>
    </div>
"""

# Card 1: Total Mahasiswa
with col1:
    if 'ID No' in filtered_df.columns:
        total_students = filtered_df['ID No'].nunique()
        st.markdown(card_style.format(title="Total Mahasiswa", value=total_students, color="#007acc"), unsafe_allow_html=True)
    else:
        st.warning("Kolom 'ID No.' tidak ditemukan dalam data.")

# Card 2: Rata-rata CGPA
with col2:
    if 'CGPA' in filtered_df.columns:
        avg_cgpa = round(filtered_df['CGPA'].mean(), 2)
        st.markdown(card_style.format(title="Rata-rata CGPA", value=avg_cgpa, color="#28a745"), unsafe_allow_html=True)
    else:
        st.warning("Kolom 'CGPA' tidak ditemukan dalam data.")

# Card 3: Tahun Masuk (YoG)
with col3:
    if not filtered_df.empty and 'YoG' in filtered_df.columns:
        min_yog = int(filtered_df['YoG'].min())
        max_yog = int(filtered_df['YoG'].max())
        year_range = f"{min_yog} – {max_yog}" if min_yog != max_yog else f"{min_yog}"
        st.markdown(card_style.format(title="Tahun Masuk (YoG)", value=year_range, color="#d9534f"), unsafe_allow_html=True)
    else:
        st.markdown(card_style.format(title="Tahun Masuk (YoG)", value="-", color="#d9534f"), unsafe_allow_html=True)


# --- Bar Chart: Rata-rata CGPA per Tahun
st.subheader("Average GPA per Year")

if 'YoG' in filtered_df.columns and 'CGPA' in filtered_df.columns:
    if not filtered_df.empty:
        avg_gpa = filtered_df.groupby('YoG')['CGPA'].mean()
        fig_bar, ax_bar = plt.subplots(figsize=(4.5, 2.5))  # Ukuran visual seimbang
        avg_gpa.plot(kind='bar', color='skyblue', ax=ax_bar)
        ax_bar.set_ylabel("Average GPA", fontsize=9)
        ax_bar.set_xlabel("Year Of Admission Masuk", fontsize=9)
        ax_bar.set_title("Average GPA", fontsize=10)
        ax_bar.tick_params(labelsize=8)
        st.pyplot(fig_bar)
    else:
        st.info("Tidak ada data untuk kombinasi filter.")
else:
    st.warning("Kolom 'YoG' atau 'CGPA' tidak ditemukan dalam data.")

# --- Pie Chart: Distribusi Gender
st.subheader("Distributed Gender")

if 'Gender' in filtered_df.columns:
    gender_counts = filtered_df['Gender'].value_counts()
    if not gender_counts.empty:
        fig_pie, ax_pie = plt.subplots(figsize=(3.5, 3.5))  # Pie lebih kecil & pas
        ax_pie.pie(
            gender_counts,
            labels=gender_counts.index,
            autopct='%1.1f%%',
            startangle=90,
            colors=['lightcoral', 'lightskyblue'],
            textprops={'fontsize': 8}  # Font kecil
        )
        ax_pie.axis('equal')
        st.pyplot(fig_pie)
    else:
        st.info("Tidak ada data gender pada filter ini.")
else:
    st.warning("Kolom 'Gender' tidak ditemukan dalam data.")
# --- Box Plot CGPA100–CGPA400 per Tahun Masuk ---
# --- Box Plot CGPA100–CGPA400 per Tahun Masuk ---
st.subheader("Distributed GPA year 1 - 4 per Year Of Admission")

# Pastikan kolom tersedia
cgpa_columns = ['CGPA100', 'CGPA200', 'CGPA300', 'CGPA400']
available_cols = [col for col in cgpa_columns if col in filtered_df.columns]

if available_cols and 'YoG' in filtered_df.columns:
    if not filtered_df.empty:
        # Persiapkan data untuk boxplot: melt jadi long-form
        boxplot_data = pd.melt(
            filtered_df,
            id_vars=['YoG'],
            value_vars=available_cols,
            var_name='Level',
            value_name='GPA'
        )

        # Plot dengan Matplotlib/Seaborn (ukuran default)
        import seaborn as sns
        fig_box, ax_box = plt.subplots(figsize=(7, 4))  # Ukuran sebelum dikecilkan
        sns.boxplot(data=boxplot_data, x='YoG', y='Nilai GPA', hue='Level', ax=ax_box)
        ax_box.set_title("Distributed GPA year 1 - 4 per Year Of Admission")
        ax_box.legend(title='Tingkat')
        st.pyplot(fig_box)
    else:
        st.info("Tidak ada data untuk ditampilkan dalam box plot.")
else:
    st.warning("Kolom 'YoG' atau CGPA100–400 tidak tersedia dalam data.")
